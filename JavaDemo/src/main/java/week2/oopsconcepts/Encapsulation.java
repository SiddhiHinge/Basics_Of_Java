package week2.oopsconcepts;

public class Encapsulation {
    private int num;

    public int getNum() {
        return num;
    }

    public void setNum(int obj_num) {
        num = obj_num;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String obj_str) {
        str = obj_str;
    }

    private String str;
}
